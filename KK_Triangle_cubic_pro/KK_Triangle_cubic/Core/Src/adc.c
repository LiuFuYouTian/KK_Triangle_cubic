uint16_t ADC_Value[120];
Battery_TYPE BAT = {0,0,0};

void GET_BAT_VOL(void)
{
	BAT.S1 = 0;BAT.S2 = 0;BAT.S3 = 0;
	for(uint8_t i = 0; i < 120;){
		BAT.S1 += ADC_Value[i++];
		BAT.S2 += ADC_Value[i++];
		BAT.S3 += ADC_Value[i++];
	}
	BAT.S1 /= 40;
	BAT.S2 /= 40;
	BAT.S3 /= 40;
	
	BAT.S1 *= (double)(1.431*3.3/4095);
	BAT.S2 *= (double)(2.754*3.3/4095);
	BAT.S3 *= (double)(3.811*3.3/4095);
	
	BAT.S3 -= BAT.S2;
	BAT.S2 -= BAT.S1;
	
//	printf("[\tmain]info:BAT.S1=%1.3fV\r\n", BAT.S1);
//	printf("[\tmain]info:BAT.S2=%1.3fV\r\n", BAT.S2);
//	printf("[\tmain]info:BAT.S3=%1.3fV\r\n", BAT.S3);
}


