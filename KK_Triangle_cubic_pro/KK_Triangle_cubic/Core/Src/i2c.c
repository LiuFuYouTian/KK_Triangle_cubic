/************************************************************
往从机中的寄存器写入多个数据
*addr：器件的地址
*reg：首个写入数据的寄存器地址
*len：要写入的数据的个数
*buf：要写入的数据区的首地址
*返回值：成功写入则返回0，失败则返回1
**************************************************************/
uint8_t MoniI2c_WriteSomeDataToSlave(uint8_t addr,uint8_t reg,uint8_t len,uint8_t *buf)
{
  HAL_StatusTypeDef status = HAL_OK;
  
  status = HAL_I2C_Mem_Write(&hi2c1, MPU6050_SLAVE_ADDRESS, (uint16_t)reg, I2C_MEMADD_SIZE_8BIT, buf, len, 100); 

  /* 检测I2C通信状态 */
  if(status != HAL_OK)
  {
    /* 调用I2C通信错误处理函数 */
    return 1;
  }        
  return 0;
}   

/************************************************************************
从从机中读取采样到的高8位和低8位的数据

*addr：器件的地址
*reg：要读取数据的首个数据的寄存器地址
*len：读取的数据个数
*buf：读取到的8位数据存放区的地址，即通过指针的方式将读取到的数据返回给调用者。
*返回值：成功读取则返回0，失败则返回1
*************************************************************************/
uint8_t MoniI2c_ReadSomeDataFromSlave(uint8_t addr,uint8_t reg,uint8_t len,uint8_t *buf)
{
	HAL_StatusTypeDef status = HAL_OK;

  status = HAL_I2C_Mem_Read(&hi2c1, MPU6050_SLAVE_ADDRESS, (uint16_t)reg, I2C_MEMADD_SIZE_8BIT, buf, len, 100);
  
  /* 检测I2C通信状态 */
  if(status != HAL_OK)
  {
    /* 调用I2C通信错误处理函数 */
    return 1;
  }        
  return 0;
}
